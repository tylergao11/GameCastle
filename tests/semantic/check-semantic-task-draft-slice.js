var assert = require('assert');
var dictionary = require('../../packages/semantic/src/capability-semantic-dictionary');
var draftApi = require('../../packages/semantic/src/semantic-draft');
var referencesApi = require('../../packages/semantic/src/semantic-reference-runtime');
var taskPlan = require('../../packages/semantic/src/semantic-task-plan');
var sliceApi = require('../../packages/semantic/src/semantic-task-draft-slice');
var seedLoader = require('../../packages/semantic/src/semantic-seed-loader');
var fs = require('fs');
var path = require('path');

var index = dictionary.loadIndex();
var source = seedLoader.load(fs.readFileSync(path.join(__dirname, '..', 'fixtures', 'semantic', 'semantic-snake-state-seed.dsl'), 'utf8'), index);
var draft = draftApi.create(referencesApi.create(index), source);
var plan = taskPlan.create([{ type: 'plan-task', semanticId: 'fields', goal: 'Add a flag', after: [] }]);
var slice = sliceApi.create(draft, plan, 'fields');
assert.strictEqual(slice.taskId, 'fields');
assert.strictEqual(slice.goal, 'Add a flag');
assert.strictEqual(slice.workMode, 'revision', 'seeded draft is revision board');
assert(slice.index.entities.some(function(entity) { return entity.semanticId === 'GameState'; }));
var gameState = slice.index.entities.filter(function(entity) { return entity.semanticId === 'GameState'; })[0];
assert(gameState.members.some(function(member) { return member.semanticId === 'score' && member.value === 0; }), 'revision slice projects member values');
assert(Object.isFrozen(slice));
var emptyDraft = draftApi.create(referencesApi.create(index), null);
var emptyPlan = taskPlan.create([{ type: 'plan-task', semanticId: 'core', goal: 'Create only the core model.', after: [] }]);
var emptySlice = sliceApi.create(emptyDraft, emptyPlan, 'core');
assert.strictEqual(emptySlice.workMode, 'new', 'empty draft is new board');
console.log('[SemanticTaskDraftSlice] dispatch-task slice projects revision board + member values passed');
