var assert = require('assert');
var childProcess = require('child_process');
var fs = require('fs');
var path = require('path');

var ROOT = path.join(__dirname, '..');
var OUTPUT_DIR = path.join(ROOT, 'output');

function run(args, label) {
  var result = childProcess.spawnSync(process.execPath, args, {
    cwd: ROOT,
    encoding: 'utf8',
  });
  if (result.status !== 0) {
    throw new Error(label + ' failed\nSTDOUT:\n' + result.stdout + '\nSTDERR:\n' + result.stderr);
  }
  return result.stdout;
}

function readJson(fileName) {
  return JSON.parse(fs.readFileSync(path.join(OUTPUT_DIR, fileName), 'utf8'));
}

function readText(fileName) {
  return fs.readFileSync(path.join(OUTPUT_DIR, fileName), 'utf8');
}

function assertExists(fileName) {
  assert(fs.existsSync(path.join(OUTPUT_DIR, fileName)), fileName + ' should exist');
}

function main() {
  var stdout = run([
    'ai/pipeline.js',
    '--intent-fixture-file',
    'ai/fixtures/intent-parkour-real.dsl',
    '--batch-label',
    'semantic_playtest_pipeline_output',
  ], 'semantic playtest pipeline output');
  assert(stdout.indexOf('[SemanticPlaytest]') >= 0, 'pipeline should run SemanticPlaytestAgent automatically');

  [
    'semantic-playtest-report.json',
    'semantic-playtest-policy.json',
    'semantic-playtest-llm-report.json',
    'semantic-playtest-user-report.json',
    'intent-world-view.json',
    'semantic-playtest-repair.intent.dsl',
  ].forEach(assertExists);

  var report = readJson('semantic-playtest-report.json');
  var policy = readJson('semantic-playtest-policy.json');
  var llmReport = readJson('semantic-playtest-llm-report.json');
  var userReport = readJson('semantic-playtest-user-report.json');
  var view = readJson('intent-world-view.json');
  var repairIntent = readText('semantic-playtest-repair.intent.dsl');

  assert.strictEqual(report.owner, 'SemanticPlaytestAgent', 'pipeline report should be owned by SemanticPlaytestAgent');
  assert.strictEqual(policy.owner, 'SemanticPlaytestAgent', 'pipeline policy should be generated by SemanticPlaytestAgent');
  assert.strictEqual(llmReport.audience, 'llm', 'pipeline should write LLM report');
  assert.strictEqual(userReport.audience, 'user', 'pipeline should write user report');
  assert.strictEqual(view.owner, 'IntentWorldView', 'pipeline should write IntentWorldView');
  assert.strictEqual(view.sceneIntent.uiPolicy.role, 'supporting layer only', 'IntentWorldView should keep UI supporting');
  assert(view.semanticRepairRecommendations.some(function(action) {
    return action.action === 'apply_semantic_repair' &&
      action.experienceDimension === 'reward_pacing' &&
      action.repairVerb === 'increase_presence';
  }), 'IntentWorldView should recommend gameplay repair through the unified semantic action');
  assert(report.tickReport.eventLog.length > 0, 'pipeline report should include tick EventLog');
  assert(report.tickReport.snapshots.length > 0, 'pipeline report should include snapshots');
  assert(llmReport.tickIssues[0].evidence && typeof llmReport.tickIssues[0].evidence.tick === 'number', 'LLM report should preserve tick evidence');
  assert(userReport.issues[0].indexOf('Tick ') >= 0, 'user report should present tick evidence in human language');
  assert(repairIntent.indexOf('place ') >= 0, 'pipeline should write executable repair Intent DSL');

  var safeText = JSON.stringify(report.input);
  assert(safeText.indexOf('"x"') < 0, 'agent input should not expose x coordinates');
  assert(safeText.indexOf('"y"') < 0, 'agent input should not expose y coordinates');
  assert(safeText.indexOf('bridgePlan') < 0, 'agent input should not expose bridge plan');
  assert(safeText.indexOf('componentId') < 0, 'agent input should not expose component ids');
  var viewText = JSON.stringify(view);
  assert(viewText.indexOf('"x"') < 0, 'IntentWorldView should not expose x coordinates');
  assert(viewText.indexOf('"y"') < 0, 'IntentWorldView should not expose y coordinates');
  assert(viewText.indexOf('bridgePlan') < 0, 'IntentWorldView should not expose bridge plan');
  assert(viewText.indexOf('componentId') < 0, 'IntentWorldView should not expose component ids');

  console.log('[SemanticPlaytestPipelineOutput] pipeline writes policy, dual report, intent world view, tick evidence, repair intent');
}

main();
