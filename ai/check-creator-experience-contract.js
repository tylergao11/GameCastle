var assert = require('assert');
var fs = require('fs');
var path = require('path');
var contract = require('../shared/creator-experience-contract.json');
var creator = require('../server/local-runtime/creator-experience');
var root = path.resolve(__dirname, '..');
assert.strictEqual(contract.contractId, 'gamecastle.creator-experience');
assert.deepStrictEqual(contract.stages, Object.keys(creator.STAGES));
contract.debtKinds.forEach(function(kind) { assert(creator.recovery(kind === 'build-debt' ? 'UNKNOWN' : kind === 'asset-debt' ? 'ASSET_WEAVE_REJECTED' : kind === 'provider-budget' ? 'PROVIDER_BUDGET_EXHAUSTED' : kind === 'cancelled' ? 'RUN_CANCELLED' : kind === 'build-in-progress' ? 'RUN_BUSY' : 'CONTINUE_STATE_MISSING').kind === kind); });
['platform/src/pages/GameCastleExperience.tsx', 'platform/src/runtime/useLocalGameRuntime.ts', 'server/local-runtime/creator-experience.js'].forEach(function(file) { assert(fs.existsSync(path.join(root, file)), 'missing CreatorExperience owner file: ' + file); });
console.log('[CreatorExperienceContract] stages, actionable debt, stable API owners, and UI boundary passed');
