var assert = require('assert');
var vm = require('vm');
var code = require('./asset-runtime-overlay-codegen').generate({ bindings: [{ binding: 'asset.hero', asset: { path: 'assets/generated/hero.png' } }, { binding: 'asset.key', asset: { path: 'assets/generated/key.png' } }] });
var project = { resources: { resources: [], resourceFolders: [] }, layouts: [{ objects: [], instances: [], usedResources: [], layers: [{ name: 'UI' }] }] };
var context = { window: {}, gdjs: { projectData: project } }; context.window.gdjs = context.gdjs;
vm.runInNewContext(code, context);
assert.equal(project.resources.resources.length, 2);
assert.equal(project.resources.resources[0].name, 'gc_asset_hero_hero.png');
assert.equal(project.resources.resources[0].file, 'assets/generated/hero.png');
assert.equal(project.resources.resources[0].alwaysLoaded, true);
assert.deepEqual(project.layouts[0].usedResources, [{ name: 'gc_asset_hero_hero.png' }, { name: 'gc_asset_key_key.png' }]);
assert.equal(project.layouts[0].objects.length, 2);
assert.equal(project.layouts[0].objects[0].type, 'Sprite');
assert.equal(project.layouts[0].objects[0].updateIfNotVisible, false);
assert.equal(project.layouts[0].objects[0].animations[0].directions[0].sprites[0].image, 'gc_asset_hero_hero.png');
assert.equal(project.layouts[0].instances.length, 2);
assert.equal(project.layouts[0].instances[0].layer, 'UI');
assert.equal(context.window.GameCastleAssetRuntime.injected, true);
console.log('[AssetRuntimeOverlayRuntime] bindings become GDevelop Sprite resources and instances');
