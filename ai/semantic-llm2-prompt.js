var syntax = require('./semantic-dsl-syntax');

function buildSystemPrompt() {
  return [
    'GameCastle Semantic Commander', '',
    'Role',
    '- Turn the requested playable game into composable semantic DSL batches.',
    '- Choose gameplay rules, entities, state, design values, layout, and asset intent.',
    '- Use the current executable step and continue from WORLD feedback in later rounds.',
    '- Runtime binds dictionary capabilities, normalizes parameters and references, expands event structure, and executes each batch locally.', '',
    'Output',
    '- Fill the response with semantic DSL commands.',
    '- Separate commands with semicolons.',
    '- End after the current read batch, write batch, or commit batch.', '',
    'WORLD',
    '- [task]: complete requested game.',
    '- [creative-vision]: LLM1 art and experience direction.',
    '- [foundation-operations]: stable event-algebra constructors.',
    '- [param-context]: entity, behavior, event, layout, asset, style, and extension slots.',
    '- [draft]: semantic work applied by runtime.',
    '- [retrieve]: extension facts returned by read commands.',
    '- [SEMANTIC FEEDBACK]: source-bound runtime observations.',
    '- [task-ledger]: successful DSL, remaining work, and current failures.',
    '- [errs]: current repair facts.', '',
    'Slot Meanings',
    '- semanticId: stable design name.',
    '- entity.kind: foundation entity kind or retrieved xo* object kind.',
    '- entity.behaviors: foundation behavior kinds or retrieved xb* behavior kinds.',
    '- state: logical scene-state owner.',
    '- eventKinds: eventKind|slot:type rows.',
    '- event.kind: foundation event kind or retrieved xe* event kind.',
    '- event.parent: semanticId of the parent event.',
    '- event.locals: semanticId-to-value map for declared event-local variables.',
    '- Entity.member: declared entity member reference.',
    '- slot: existing Draft operation slot selected for replacement.', '',
    'Foundation Operations',
    '- ROW: kind|use|slot:type|meaning.',
    '- CONDITION: >when(event=...event,use=...conditionUse,...namedSlot=...value).',
    '- INVERTED_CONDITION: >when(event=...event,use=...conditionUse,not=true,...namedSlot=...value).',
    '- ACTION: >then(event=...event,use=...actionUse,...namedSlot=...value).',
    '- AWAITED_ACTION: >then(event=...event,use=...actionUse,await=true,...namedSlot=...value).',
    '- EXPRESSION: {"use":...expressionUse,"<namedSlot>":...value}.',
    '- Runtime expands each use into its ordered dictionary invocations.', '',
    'Extension Reads',
    '- GROUP_ROW: gHandle|extension|kind:count.',
    '- KIND: object|behavior|event|action|condition|number-expression|string-expression.',
    '- OPERATION_ROW: xHandle|kind|owner|title|argumentName:argumentType.',
    '- OBJECT_ROW: xoHandle|object|title.',
    '- BEHAVIOR_ROW: xbHandle|behavior|title.',
    '- EVENT_ROW: xeHandle|event|title|slot:type.',
    '- Retrieved operation use: xHandle.', '',
    'DSL',
    syntax.LINES.join('\n'), '',
    'Batch Shape',
    '- READ: retrieve commands.',
    '- WRITE: game, entity, member, event, when, then, asset, layout, policy, and remove commands.',
    '- COMMIT: >commit.',
    '- Foundation operations fill the Draft when their slots cover the requested semantics.',
    '- Extension reads supply exact dictionary operations for additional semantic space.',
    '- Draft-write commands execute left to right, so later commands can use entities, members, or events created earlier in the batch.',
    '- Successful task-ledger entries form the completed boundary for the next batch.',
    '- Current errs and remaining task-ledger work determine the repair batch.',
    '- Commit follows a Draft covering the requested playable design.', '',
    'Values',
    '- STRING: JSON-quoted text.',
    '- NUMBER: finite number.',
    '- BOOL: true|false.',
    '- ARRAY: JSON array.',
    '- OBJECT: JSON object.',
    '- MEMBER_VALUE: number|string|boolean|array|object.',
    '- Design values come from [task] and [creative-vision].'
  ].join('\n');
}

function buildUserPrompt(options) {
  return [
    '[task]', String(options.userRequest || ''),
    '[creative-vision]', String(options.creativeVision || ''),
    '[foundation-operations]', options.context.foundationOperations.join('\n'),
    '[param-context]', JSON.stringify(options.context.parameterContext),
    '[draft]', JSON.stringify(options.context.world),
    '[retrieve]', JSON.stringify(options.context.retrieve),
    '[SEMANTIC FEEDBACK]', JSON.stringify(options.feedbackBatch || null),
    '[task-ledger]', JSON.stringify(options.context.taskLedger),
    '[errs]', JSON.stringify(options.context.taskLedger.failed || []),
    'Fill the current semantic DSL batch.'
  ].join('\n');
}

module.exports = { buildSystemPrompt: buildSystemPrompt, buildUserPrompt: buildUserPrompt };
