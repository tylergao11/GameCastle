var assert = require('assert').strict;
var fs = require('fs');
var os = require('os');
var path = require('path');
var intake = require('./template-intake');
var normalizer = require('./template-normalizer');

var sourceRoot = process.env.GAMECASTLE_GDEVELOP_SOURCE_DIR || path.resolve(__dirname, '..', '..', 'GDevelop-master');
var gamesRoot = path.join(sourceRoot, 'GDJS', 'tests', 'games');
assert(fs.existsSync(gamesRoot), 'GDJS tests source missing; set GAMECASTLE_GDEVELOP_SOURCE_DIR.');
var record = intake.intake({ sourceId: 'gdevelop.gdjs-tests', rootDir: gamesRoot, entryProject: 'SaveLoadExample/Save & load example.json', upstreamRevision: 'test-source-v1' });
assert.equal(record.intakeStatus, 'quarantined');
var first = normalizer.normalize(record, { rootDir: gamesRoot });
var second = normalizer.normalize(record, { rootDir: gamesRoot });
assert.equal(first.normalizationHash, second.normalizationHash, 'normalization must replay deterministically');
assert(first.objects.length > 0 && first.behaviors.length > 0 && first.eventGraph.nodes.length > 0 && first.animationGraphs.length > 0, 'normalizer must project playable structure');
assert.equal(first.lossAccounting.blockingSourceNodes.length, 0);
assert.throws(function() { intake.intake({ sourceId: 'gdevelop.official-template-catalog', entryProject: 'x.json' }); }, function(error) { return error && error.code === 'TEMPLATE_LICENSE_DISCOVERY_ONLY'; });
var temp = fs.mkdtempSync(path.join(os.tmpdir(), 'gamecastle-template-'));
fs.writeFileSync(path.join(temp, 'bad.json'), '{', 'utf8');
var malformed = Object.assign({}, record, { entryProject: 'bad.json', contentHash: 'bad-source-hash' });
var invalid = normalizer.normalize(malformed, { rootDir: temp });
assert(invalid.diagnostics.some(function(item) { return item.code === 'UNSUPPORTED_PROJECT_SCHEMA' && item.blocking; }), 'invalid source must fail closed');
console.log('[TemplateIntakeNormalizer] hash, rights quarantine, deterministic structural IR, animation/event projection, and invalid-source failure passed');
